import random
print('โปรเเกรมเป่ายิงฉุบ')
while True:
    a = random.choice(["ค้อน","กรรไกร","กระดาษ"])
    b = input('เลือก ค้อน กรรไกร กระดาษ\nคุณเลือก :')
    if a == b :
        print("ผลลัพธ์ คือ เสมอ")
    elif b == "ค้อน" and a == "กรรไกร":
        print("ผลลัพธ์ คือ คุณชนะ")
    elif b == "กรรไกร" and a == "กระดาษ":
        print("ผลลัพธ์ คือ คุณชนะ")
    elif b == "กระดาษ" and a == "ค้อน":
        print("ผลลัพธ์ คือ คุณชนะ")
    else:
        print("ผลลัพธ์ คือ คุณเเพ้")
    